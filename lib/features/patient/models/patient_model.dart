class Patient {
  final String name;
  final String contactNo;
  final String whatsappNo;
  final String address;
  final String email;
  final String chiefComplain;
  final String familyDisease;
  final String otHistory;
  final String presentMedication;
  final String height;
  final String weight;

  Patient({
    required this.name,
    required this.contactNo,
    required this.whatsappNo,
    required this.address,
    required this.email,
    required this.chiefComplain,
    required this.familyDisease,
    required this.otHistory,
    required this.presentMedication,
    required this.height,
    required this.weight,
  });
}
